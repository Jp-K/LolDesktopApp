    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        int lvl;

        if(!isInteger(jTextFieldLevel.getText())){
            showMessageDialog(null, "Valor digitado é diferente de um numero inteiro !!!");
        } else{
            lvl = Integer.parseInt(jTextFieldLevel.getText());
            
            if(lvl < 1 || lvl > 18){
                showMessageDialog(null, "Digite um valor válido para o nível !!!");
            }else{
                setLabels(itens, champ, lvl-1);
            }
        }
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

