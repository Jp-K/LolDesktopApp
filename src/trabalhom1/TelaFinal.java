    private void setLabels(ItensStats[] itens, CampeaoStats champ, int lvl){
        
        jLabelStatsNome.setText(champ.getNome());
        
        Double val = champ.getHp();
        val = val + (champ.getHpperlevel() * lvl);
        for(int i = 0; i < itens.length; i++){
            val += itens[i].getVida();
        }
        jLabelStatsVida.setText(Double.toString(val));
        
        val = champ.getMp();
        val += champ.getMpperlevel() * lvl;
        for(int i = 0; i < itens.length; i++){
            val += itens[i].getMana();
        }
        
        jLabelStatsMana.setText(Double.toString(val));
        
        val = champ.getMovespeed();
        for(int i = 0; i < itens.length; i++){
            val += itens[i].getVelocidadeDeMovimento();
        }
        jLabelStatsVelocidadeDeMovimento.setText(Double.toString(val));
        
        val = champ.getArmor();
        val += champ.getArmorperlevel() * lvl;
        for(int i = 0; i < itens.length; i++){
            val += itens[i].getArmadura();
        }
        jLabelStatsArmadura.setText(String.format("%.2f",val));
        
        val = champ.getSpellblock();
        val += champ.getSpellblockperlevel()* lvl;
        for(int i = 0; i < itens.length; i++){
            val += itens[i].getResistenciaMagica();
        }
        jLabelStatsResistenciaMagica.setText(String.format("%.2f",val));
        
        jLabelStatsAlcance.setText(Double.toString(champ.getAttackrange()));
        
        val = champ.getHpregen();
        val += (champ.getHpregenperlevel() / 100) * lvl;
        jLabelStatsRegenVida.setText(String.format("%.2f",val));
        
        
        val = champ.getMpregen();
        val += (champ.getMpperlevel() / 100) * lvl;
        jLabelStatsRegenMana.setText(String.format("%.2f",val));
        
        val = champ.getCrit();
        for(int i = 0; i < itens.length; i++){
            val += itens[i].getChanceDeAcertoCritico();
        }
        jLabelStatsChanceCritico.setText(Double.toString(val));
        
        val = champ.getAttackdamage();
        val += champ.getAttackdamageperlevel() * lvl;
        for(int i = 0; i < itens.length; i++){
            val += itens[i].getDanoDeAtaque();
        }
        jLabelStatsDanoDeAtaque.setText(Double.toString(val));
        
        
        val = champ.getAttackspeed();
        val += (champ.getAttackspeedperlevel() / 100)* lvl;
        for(int i = 0; i < itens.length; i++){
            val += itens[i].getVelocidadeDeAtaque();
        }
        jLabelStatsVelocidadeDeAtaque.setText(String.format("%.2f",val));
        
        jLabelNomeItem1.setText(itens[0].getNome());
        jLabelNomeItem2.setText(itens[1].getNome());
        jLabelNomeItem3.setText(itens[2].getNome());
        jLabelNomeItem4.setText(itens[3].getNome());
        jLabelNomeItem5.setText(itens[4].getNome());
        jLabelNomeItem6.setText(itens[5].getNome());
    }

